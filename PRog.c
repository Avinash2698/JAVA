int main()
{
    int n=11;
    char s[n];
    for(int i=0;i<n;i++)
    {
        scanf("%c",&s[i]);
    }
    if(s[0]==1 && s[1]==2)
     {
         if(s[8]=='A' && s[9]=='M')
         {
         s[0]=0;
         s[1]=0;
         s[8]=' ';
         s[9]=' ';
         }
     }
     if(s[0]==1 && s[1]==2)
     {
         if(s[8]=='P' && s[9]=='M')
         {
         s[0]=1;
         s[1]=2;
         s[8]=' ';
         s[9]=' ';
         }
     }   
    if(s[0]!=1 && s[1]!=2)
    {
    s[0]=s[0]+1;
    s[1]=s[1]+2;
    s[8]=' ';
    s[9]=' ';
    }
    printf("%s",s);
return 0;
}
